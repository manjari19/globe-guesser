export function normalizeGuess(input, target) {
  const guess = input.trim().toLowerCase();
  const targetLower = target.toLowerCase();

  // Direct match
  if (guess === targetLower) return true;

  // Check alternate names
  const mapped = ALTERNATE_NAMES[guess];
  if (mapped && mapped.toLowerCase() === targetLower) return true;

  // Fuzzy: remove "the", "republic of", "democratic republic of", "peoples republic of" etc.
  const strip = (s) => s
    .replace(/^(the|republic of|democratic republic of|people's republic of|peoples republic of|kingdom of|state of|united states of|federation of)\s+/i, '')
    .replace(/\s+(republic|federation|kingdom|state|islands|island|and dependencies)$/i, '')
    .trim();

  if (strip(guess) === strip(targetLower)) return true;

  // Levenshtein distance for typos (allow 1 typo for short, 2 for longer)
  const distance = levenshtein(guess, targetLower);
  const maxAllowed = targetLower.length > 8 ? 2 : 1;
  if (distance <= maxAllowed) return true;

  return false;
}

function levenshtein(a, b) {
  const dp = Array.from({ length: a.length + 1 }, (_, i) =>
    Array.from({ length: b.length + 1 }, (_, j) => (i === 0 ? j : j === 0 ? i : 0))
  );
  for (let i = 1; i <= a.length; i++) {
    for (let j = 1; j <= b.length; j++) {
      dp[i][j] = a[i - 1] === b[j - 1]
        ? dp[i - 1][j - 1]
        : 1 + Math.min(dp[i - 1][j], dp[i][j - 1], dp[i - 1][j - 1]);
    }
  }
  return dp[a.length][b.length];
}
